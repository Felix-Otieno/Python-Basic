number1 = 100
number2 = 20

number1 += number2
print(number1)  # This prints 120

number1 -= number2
print(number1)  # This prints 100

number1 *= number2
print(number1)  # This prints 2000

number1 /= number2
print(number1)  # This prints 100.0

number1 //= number2
print(number1)  # This prints 5.0

number1 %= number2
print(number1)  # This prints 5.0

number1 **= number2
print(number1)  # This prints 95367431640625.0
